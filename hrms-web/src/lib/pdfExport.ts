export const exportCaseStudyToPDF = (caseStudy: any) => {
  const doc = new jsPDF();
  
  doc.setFontSize(20);
  doc.text('Case Study', 14, 20);
  
  doc.setFontSize(12);
  doc.text(`Case #: ${caseStudy.case_number || 'N/A'}`, 14, 35);
  doc.text(`Status: ${caseStudy.status || 'N/A'}`, 14, 45);
  doc.text(`Verdict: ${caseStudy.verdict || 'N/A'}`, 14, 55);
  doc.text(`Created: ${caseStudy.created_at ? new Date(caseStudy.created_at).toLocaleDateString() : 'N/A'}`, 14, 65);

  let yPos = 80;

  const addSection = (label: string, value?: string) => {
    if (!value) return;
    doc.setFontSize(14);
    doc.text(`${label}:`, 14, yPos);
    doc.setFontSize(11);
    const splitText = doc.splitTextToSize(value, 180);
    doc.text(splitText, 14, yPos + 10);
    yPos += 10 + (splitText.length * 7);
  };

  addSection('Charges', caseStudy.charges_text || '');
  addSection('Allegations', caseStudy.allegations || '');
  addSection('Plea', caseStudy.plea || '');
  addSection('Statement', caseStudy.statement || '');
  addSection('Sanctions', caseStudy.sanctions_imposed || '');
  addSection('Final Verdict Notes', caseStudy.final_verdict_text || '');
  
  doc.setFontSize(8);
  doc.text('Generated by YARA HRMS', 14, doc.internal.pageSize.height - 10);
  
  doc.save(`CaseStudy_${caseStudy.case_number || 'export'}.pdf`);
};

