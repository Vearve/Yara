// Export table data to PDF with auto table
export const exportTableToPDF = (
  title: string,
  columns: string[],
  data: any[][],
  filename: string
) => {
  const doc = new jsPDF();
  
  doc.setFontSize(18);
  doc.text(title, 14, 15);
  
  autoTable(doc, {
    head: [columns],
    body: data,
    startY: 25,
    theme: 'grid',
    styles: { fontSize: 10 },
    headStyles: { fillColor: [212, 175, 55] },
  });
  
  doc.setFontSize(8);
  doc.text('Generated by YARA HRMS', 14, doc.internal.pageSize.height - 10);
  
  doc.save(filename);
};
